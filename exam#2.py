def add(a, b):
    return (a+b)
def substract(a, b):
    return (a-b)
def multiply(a, b):
    return (a*b)
def divide(a, b):
    return (a/b)

print("1 = add, 2 = substract, 3 = multiply, 4 = divide")
a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))
1 == add
2 == substract
3 == multiply
4 == divide
function = int(input("Enter the number 1,2,3, or 4 to do the operation: "))

if function == 1:
    print(a+b)
elif function == 2:
    print(a-b)
elif function == 3:
    print(a*b)
else:
    print(a/b)

